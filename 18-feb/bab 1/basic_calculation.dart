void main() {
  double suhuKotaA = 30.5;
  double suhuKotaB = 28.0;

  if (suhuKotaA > suhuKotaB) {
    print("Kota A lebih panas daripada Kota B.");
  } else if (suhuKotaA < suhuKotaB) {
    print("Kota B lebih panas daripada Kota A.");
  } else {
    print("Kota A dan Kota B memiliki suhu yang sama.");
  }
}
